import 'package:flutter/material.dart';

class AppbarTheme {
  AppbarTheme._();

  /*---------------> Light appbar <--------------*/
  static const lightAppbarTheme = AppBarTheme(
    backgroundColor: Colors.transparent,
    iconTheme: IconThemeData(color: Colors.white),
  );

  /*---------------> Dark appbar <--------------*/
  static const darkAppbarTheme = AppBarTheme(
    backgroundColor: Colors.transparent,
    iconTheme: IconThemeData(color: Colors.white),
  );
}
